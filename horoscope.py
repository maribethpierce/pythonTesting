def horoscope(month):
    '''Return horoscope for given date.'''
    if month == 1:
        return 'Capricorn'
    elif month == 2:
         return 'Aquarius'
    elif month == 3:
         return 'Pisces'
    elif month == 4:
         return 'Aries'
    elif month == 5:
         return 'Taurus'
    elif month == 6:
         return 'Gemini'
    elif month == 7:
         return 'Cancer'
    elif month == 8:
         return 'Leo'
    elif month == 9:
         return 'Virgo'
    elif month == 10:
         return 'Libra'
    elif month == 11:
         return 'Scorpio'
    elif month == 12:
         return 'Sagittarius'

